!| LU           |-->| LOGIC UNIT IDENTIFIER FOR WRITING                |
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
