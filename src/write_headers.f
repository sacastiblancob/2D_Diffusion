     &16X,'                                        ',/,
     &16X,'><><><><><><><><><><><><><><><><><><><><',/,
     &16X,'~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~',/,
     &16X,/)
