     &16X,'N NNN     S       F     D   D',/,                    
     &16X,'N  NN SSSSS       F     DDDD ',/,                    
     &16X,'                             ',/,                    
     &16X,'  2D VERSION , FORTRAN 2003  ',/,
     &16X,'                             ',/,
     &16X,'~~~~~~~~~~~~~~~~~~~~~~~~~~~~~',/,
     &16X,/)
