     &16X,'~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~',/,
     &16X,'^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^',/,
