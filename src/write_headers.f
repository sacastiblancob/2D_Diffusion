!                     ************************
                      SUBROUTINE WRITE_HEADERS
!                     ************************
!
     &(LU)
!
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
! NAVIER-STOKES FINITE DIFFERENCES SOLVER
!~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
!
!brief     WRITE HEADERS
!
